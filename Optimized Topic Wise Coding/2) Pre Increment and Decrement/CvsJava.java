class CvsJava
{
public static void main(String args[])
{
int var1;
var1 = 100;
System.out.println("Post Increment: " + (var1++));
System.out.println("Pre Increment: " + (++var1)); //Works
System.out.println("Post Decrement: " + (var1--));
System.out.println("Pre Decrement: " + (--var1)); //Works
}
}